const (
	navy  = "\033[38;5;21m"
	blue  = "\033[38;5;69m"
	reset = "\033[0m"
)

// PrintLogo writes the ASCII art logo to w.
func PrintLogo(w io.Writer, version string, color bool) {
	logo := fmt.Sprintf(LogoTemplate, version)
	if color {
		fmt.Fprintf(w, "%s%s%s", blue, logo, reset)
	} else {
		fmt.Fprint(w, logo)
	}
}
